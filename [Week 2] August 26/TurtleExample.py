import turtle

# Turtle Properties
turtle.shape('turtle')
turtle.speed(2)
turtle.pencolor('green')
turtle.pensize(5)
turtle.fillcolor('green')
turtle.begin_fill()

# Star Shape
turtle.forward(100)
turtle.right(144)
turtle.forward(100)
turtle.right(144)
turtle.forward(100)
turtle.right(144)
turtle.forward(100)
turtle.right(144)
turtle.forward(100)
turtle.end_fill() # End
